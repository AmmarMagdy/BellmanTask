import UIKit

extension UIColor {
    
    class func accentColor() -> UIColor {
            return #colorLiteral(red: 0.8470588235, green: 0.07058823529, blue: 0.2705882353, alpha: 1)
    }

    class func primaryColor() -> UIColor {
            return #colorLiteral(red: 0.9921568627, green: 0.9490196078, blue: 0.9607843137, alpha: 1)
    }
    
}
